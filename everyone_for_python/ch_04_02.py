# １６進数のリテラルを入力
print(0x1ff)

# １０進数の数値を１６進数相当の文字列に変換
print(hex(1023))

# １６進数相当の文字列を数値に変換
print(int("0x100", 16))

# ２進数のリテラルを入力
print(0b1000)

# １０進数の数値を２進数相当の文字列に変換
print(bin(1023))

# ２進数相当の文字列を数値に変換
print(int("0b1111111111", 2))

# ８進数のリテラルを入力
print(0o17777)

# １０進数ん数値を８進数相当の文字列に変換
print(oct(1023))

# ８進数相当の文字列を数値に変換
print(int("0o1777", 8))
